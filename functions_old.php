function register_styles() {

  if (!is_admin()) {

		// register main stylesheet
		wp_register_style( 'bone-stylesheet', get_stylesheet_directory_uri() . '/library/style.css', array(), '', 'all' );

		// enqueue styles and scripts
		wp_enqueue_style( 'bone-stylesheet' );

	}
  
}

